package com.example.ar_store_shop;

public class ShopItems {


    private String ShopItems;
    private int imgItems;
    private String Amount;

    public String getShopItems() {
        return ShopItems;
    }
    public void setShopItems(String items) {
        ShopItems = items;
    }
    public int getImgItems() {
        return imgItems;
    }
    public void setImgItems(int img) {
        this.imgItems = img;
    }

    public String getAmount() { return Amount; }
    public void setAmount(String amt) {
        this.Amount = amt;
    }


}